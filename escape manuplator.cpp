#include <iostream>
using namespace std;

int main() {
    cout << "Hello, World!\n";         // Newline escape sequence
    cout << "Tab\tSpace\n";            // Tab escape sequence
    cout << "This is a \"quote\".\n";  // Double quote escape sequence
    cout << "Backslash: \\" << endl;   // Backslash escape sequence
    return 0;
}

